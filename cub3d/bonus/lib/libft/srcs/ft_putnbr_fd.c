#include "../inc/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	i;

	if (n < 0 && n > -2147483648)
	{
		ft_putchar_fd('-', fd);
		i = -n;
	}
	else
		i = n;
	if (i > 9)
	{
		ft_putnbr_fd(i / 10, fd);
		i = i % 10;
	}
	if (n == -2147483648)
	{
		write (fd, "-2147483648", 11);
	}
	else
		ft_putchar_fd(('0' + i), fd);
}
