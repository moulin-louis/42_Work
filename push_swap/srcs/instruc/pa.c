#include "../../inc/push_swap.h"

void	pb(t_stack **lst_a, t_stack **lst_b)
{
	t_stack	*temp;

	if (*lst_b)
	{
		temp = (*lst_a);
		(*lst_a) = (*lst_b);
		(*lst_b)= temp;
	}
}